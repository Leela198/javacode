package basics;

public class Arithmeticdemo {
	static void add()
	{
		int a=15;
		int b=789;
		int c=a+b;
		System.out.println(c);
	}
	static void substraction()
	{
		int a=15;
		int b=789;
		int c=a-b;
		System.out.println(c);
	}
	static void mul()
	{
		int a=15;
		int b=789;
		int c=a*b;
		System.out.println(c);
	}
	static void div()
	{
		int a=15;
		int b=789;
		int c=a/b;
		System.out.println(c);
	}
	static void mod()
	{
		int a=15;
		int b=789;
		int c=a%b;
		System.out.println(c);
		
	}
	public static void main(String[] args)
	{
		add();
		substraction();
		mul();
		div();
		mod();
		
	}

}
